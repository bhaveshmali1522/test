#INTEGER
employee_id = 2154
print(employee_id)  
print(type(employee_id))
#FLOAT
pi_approximation = 3.141592
print(pi_approximation)  
print(type(pi_approximation))

#COMPLEX
electric_impedance = 5 + 7j
print(electric_impedance)  
print(type(electric_impedance))

#STRING
company_name = 'Tech Innovations Inc.'
print(company_name)  
print(type(company_name))

#LIST
student_grades = [85, 90, 78, 92]
print(student_grades)  
print(type(student_grades))

#TUPLE
color_palette = ('red', 'green', 255)
print(color_palette)  
print(type(color_palette))

#DICT
employee_data = {'id': 101, 'name': 'Emily', 'department': 'HR'}
print(employee_data)  
print(type(employee_data))

#SET
unique_skills = {'python', 'java', 'javascript'}
print(unique_skills)  
print(type(unique_skills))


#BOOLEAN
is_admin = True
print(is_admin)  
print(type(is_admin))

is_logged_in = False
print(is_logged_in)  
print(type(is_logged_in))


#NONE
unknown_value = None
print(unknown_value)  
print(type(unknown_value))